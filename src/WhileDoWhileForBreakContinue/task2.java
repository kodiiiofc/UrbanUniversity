package WhileDoWhileForBreakContinue;

public class task2 {
    public static void main(String[] args) {
        int startValue = 1;
        int endValue = 100;
        int result = 0;
        for (int i = startValue; i < endValue + 1; i++) {
            result += i;

        }
        System.out.println(result);
    }
}
